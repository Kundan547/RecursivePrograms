#Creating a Set in Python

# Creating a Set
set1 = set()
print("Initial blank Set: ")
print(set1)

# Creating a set ewith string
set1 = set("Appologies or Die")
print(set1)

#Creating a set by using List[]
set1 = set(["kundan",'kuldeep','trilok'])
print(set1)

#Creating a set by using Dictionary
d = {"kundan" : 21, "Kuldeep" : 22}
print(set(d))
