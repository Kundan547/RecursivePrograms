my_dictionary = {
    "name": "Avinash",
    "age": 25,
    "city": "India"
}
print(my_dictionary) 

# Adding a new key-value pair using update()
my_dictionary.update({"DOB": 2004})
print(my_dictionary) 

# Modifying an existing key-value pair using update()
my_dictionary.update({"age": 27})
print(my_dictionary)  
